# Upper case 
x = "my name is abdul and my father name is mateen"
print(x.upper())

#Lowercase
x = "MY NAME IS abdul AND MY FATHER NAME IS mateen"
print(x.lower())

# Removing Whitespace

x = "   abdul,mateen"
print(x.strip())  

# Replace string 

x = "This is data  science tutorials"
print(x.replace("This" , "Bahi"))

# Split string 

x = "This, is, data , science, tutorials"
print(x.split(","))